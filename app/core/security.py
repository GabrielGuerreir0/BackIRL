def decode_access_token(token: str, expected_type: str = "access"):

    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=[ALGORITHM])
        if payload.get("type") != expected_type:
            return None  
        return payload
    except JWTError:
        return None

